import { hello } from './index';

// -----------------------------------------------------------------------------
// Tests
// -----------------------------------------------------------------------------

describe('Return greeting message', () => {
	['Oleg', 'Alex', 'John'].forEach((name, i) => {
		test(`Test #${i + 1}`, () => {
			const message = hello(name);
			expect(message).toEqual(`Hi, ${name}!`);
		});
	});
});
